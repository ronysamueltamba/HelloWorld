import math  # module mathematics

x = 2.9
y = -2.9
print(round(x))  # mengambil nilai desimal terbesar/terkecil menjadi int / bulat
print(abs(y))  # merepresentasikan nilai positif walau nilai tersebut negatif

print(math.ceil(3.9))  # ceil = method module math, untuk merubah desimal ke bilangan bulat
print(math.floor(3.9))  # floor = method module math, merubah nilai desimal menjadi bilangan bulat tetap
