bilangan1 = input('Masukan Bilangan 1 : ')
bilangan2 = input('Masukan Bilangan 2 : ')

hasil_tambah = int(bilangan1) + int(bilangan2)
hasil_pengurangan = int(bilangan1) - int(bilangan2)
hasil_perkalian = int(bilangan1) * int(bilangan2)
hasil_pembagian = int(bilangan1) // int(bilangan2)


print(hasil_tambah, 'Dalam pertambahan')
print(hasil_pengurangan, 'Dalam pengurangan')
print(hasil_perkalian, 'Dalam Perkalian')
print(hasil_pembagian, 'Dalam pembagian')