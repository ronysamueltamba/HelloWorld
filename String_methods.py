course = 'Python for Beginners'
print(course)
print(len(course), 'Character')
print(course.upper())
print(course.lower())
print(course.find('Beginners'))
print(course.replace('Beginners', 'Absolute Beginners'))
print('Python' in course)

# len (general purpose function) = Function untuk menghitung jumlah karakter
# upper (specific function) = method untuk merbuah ke upper case
# lower (specific function) = method untuk merubah String menjadi lower case
# find (specific function) = method untuk mencari index ke berapa, P = value
# replace (specific function) = method untuk merubah string lama ke baru
# in = operator untuk mencari character, produce boolean (true or false)
